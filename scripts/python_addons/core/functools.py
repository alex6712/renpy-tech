def square(a):
    # type: (int) -> int
    """Функция возведения в квадрат.

    Возводит в квадрат число ``a`` и возвращает результат.

    Parameters
    ----------
    a : `int`
        Число, которое необходимо возвести в квадрат.

    Returns
    -------
    result : `int`
        Квадрат числа ``a``.
    """
    return a ** 2
